package com.operators;

public class UnaryOp {

    public static void main(String[] args) {
        int a=10;
        int b=10;
        int c=10;
        int d=10;
        int f=70;
        a++;
        ++b;
        c--;
        --c;
        f--;
        System.out.println(" "+a);
        System.out.println(" "+b);
        System.out.println(" "+c);
        System.out.println("f"+f);
        System.out.println(" "+d);

    }
}
